"""
Operators in Python
Python has the following operators:
Arithmetic operators
Assignment operators
Comparison operators
Logical operators
Identity operators
Membership operators
"""
# Arithmetic operator (+)
num = 10 + 20 
print(num)  # 30 

# Arithmetic operator (-)
a = 20 - 10
print(a)  # 10

# Arithmetic operator (*)
b = 10 * 20
print(b)  # 200

# Arithmetic operator (/)
c = 20 / 10
print(c)  # 2.0

# Comparison operators
print(a > b) # False
print(a < b) # True
print(a != b) # True
print(a == b) # False

# Logical operators
print (a > b and a < b) # False
print (a > b or a < b) # True

# Identity operators
x = 10
y = 10
print(x is y) # True
print(x is not y) # False

# Membership operators
list1 = [1, 2, 3, 4, 5]
list2 = [1, 2, 3, 4, 5]
# print(1 in list2) # True
# print(6 not in list2) # True
print(list2)


